#define __NAUTILUS_MAIN__

#include <arch/riscv/init.h>
#include <arch/riscv/riscv.h>


/*
 * 2nd two parameters are
 * only used if this is an HRT
 */
void
main (unsigned long mbd,
      unsigned long magic)

{
    // set M Previous Privilege mode to Supervisor, for mret.
    unsigned long x = r_mstatus();
    x &= ~MSTATUS_MPP_MASK;
    x |= MSTATUS_MPP_S;
    w_mstatus(x);

    // set M Exception Program Counter to main, for mret.
    // requires gcc -mcmodel=medany
    w_mepc((uint64_t)init);

    // disable paging for now.
    w_satp(0);

    // delegate all interrupts and exceptions to supervisor mode.
    w_medeleg(0xffff);
    w_mideleg(0xffff);
    w_sie(r_sie() | SIE_SEIE | SIE_STIE | SIE_SSIE);

    // keep each CPU's hartid in its tp register.
    int id = r_mhartid();
    w_tp(id);

    // switch to supervisor mode and jump to init().
    asm volatile("mret");
}
